package entities;

public class ItemBibliografico {
	private String titulo;
	private int anoPublicacao;
	
	public ItemBibliografico(String titulo, int anoPublicacao) {
		this.titulo = titulo;
		this.anoPublicacao = anoPublicacao;
	}
	
	// Access methods
	public String getTitulo() {
		return titulo;
	}
	public int getAnoPublicacao() {
		return anoPublicacao;
	}
	
	// Não tem setters porque um livro nunca muda o título ou ano de publicação
}
