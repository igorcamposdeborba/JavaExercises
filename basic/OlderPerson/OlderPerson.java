package vectors;

public class OlderPerson {
	private String name;
	private int age;
	
	public OlderPerson(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
}
